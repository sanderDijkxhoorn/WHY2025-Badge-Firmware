#pragma once

#include "pixel_formats.h"

#include <stdint.h>

typedef struct {
    uint32_t       w;
    uint32_t       h;
    pixel_format_t format;
    uint16_t      *pixels;
} framebuffer_t;

__attribute__((always_inline)) static inline uint16_t rgb888_to_rgb565(uint8_t r, uint8_t g, uint8_t b) {
    return ((r & 0xF8) << 8) | ((g & 0xFC) << 3) | (b >> 3);
}
